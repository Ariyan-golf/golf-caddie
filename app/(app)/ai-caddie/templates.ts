interface ClubEntry { club: Club; avgMeters: number; }

const DEFAULTS: ClubEntry[] = [
  { club: "1w", avgMeters: 210 }, { club: "3w", avgMeters: 185 },
  { club: "5w", avgMeters: 168 }, { club: "u4", avgMeters: 155 },
  { club: "u5", avgMeters: 145 }, { club: "5i", avgMeters: 137 },
  { club: "6i", avgMeters: 128 }, { club: "7i", avgMeters: 119 },
  { club: "8i", avgMeters: 110 }, { club: "9i", avgMeters: 100 },
  { club: "pw", avgMeters:  88 }, { club: "aw", avgMeters:  73 },
  { club: "sw", avgMeters:  55 }, { club: "lw", avgMeters:  35 },
];

export interface ClubInfo {
  label: string;
  avgYards: number;
  personal: boolean;
}

export function pickClub(targetYards: number, averages: ClubAverage[]): ClubInfo {
  const targetM = targetYards / 1.09361;
  const valid = averages.filter((a) => a.shot_count >= 3);
  const personal = valid.length >= 3;
  const pool: ClubEntry[] = personal
    ? valid.map((a) => ({ club: a.club, avgMeters: a.average_distance_meters }))
    : DEFAULTS;

  let best = pool[0];
  let bestScore = Infinity;
  for (const e of pool) {
    const diff = e.avgMeters - targetM;
    const score = diff >= 0 ? diff : Math.abs(diff) * 1.4;
    if (score < bestScore) { bestScore = score; best = e; }
  }

  return {
    label: CLUB_JP[best.club] ?? best.club.toUpperCase(),
    avgYards: metersToYards(best.avgMeters),
    personal,
  };
}

