export function getInstantAdvice(
  charId: CharacterId,
  yards: number,
  info: ClubInfo
): string {
  const bucket = Math.min(250, Math.max(40, Math.round(yards / 10) * 10));
  const fn = TEMPLATES[charId][bucket];
  return fn(yards, info.label, info.avgYards);
}
