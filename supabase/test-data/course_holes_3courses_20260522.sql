-- 4-1a. 3コースの合計行数（鹿屋18 + 青島18 + 湯の浦18 = 54 が期待値）
SELECT COUNT(*) AS target_rows_total
  FROM public.course_holes
 WHERE course_id IN (
   'a0511000-0000-4000-8000-000000005112',
   'a0511000-0000-4000-8000-000000005113',
   'a0511000-0000-4000-8000-000000005114'
 );
-- 期待: 54

-- 4-1b. コース別の行数（参考：大隅は触っていないので 18 のまま）
SELECT gc.name, COUNT(ch.*) AS hole_count
  FROM public.golf_courses gc
  LEFT JOIN public.course_holes ch ON ch.course_id = gc.id
 GROUP BY gc.name
 ORDER BY gc.name;
-- 期待: 大隅18 / 鹿屋18 / 青島18 / 湯の浦18 = 計72行（大隅は変化なし）

-- 4-2. パー合計・HDCP合計・距離合計（投入データの整合性チェック）
SELECT
  gc.name,
  SUM(ch.par)            AS par_total,        -- 期待: 72
  SUM(ch.hdcp)           AS hdcp_total,       -- 期待: 171（1+2+...+18）
  SUM(ch.distance_tee1)  AS tee1_total,
  SUM(ch.distance_tee2)  AS tee2_total,
  SUM(ch.distance_tee3)  AS tee3_total,
  SUM(ch.distance_tee4)  AS tee4_total
  FROM public.golf_courses gc
  JOIN public.course_holes ch ON ch.course_id = gc.id
 WHERE gc.name IN (
   '鹿児島鹿屋カントリークラブ',
   '青島ゴルフ倶楽部',
   '湯の浦カントリー倶楽部'
 )
 GROUP BY gc.name
 ORDER BY gc.name;
-- 期待値:
--   鹿屋    par=72 hdcp=171 tee1=6436 tee2=6309 tee3=6103 tee4=5186
--   青島    par=72 hdcp=171 tee1=7018 tee2=6634 tee3=6234 tee4=4652
--   湯の浦  par=72 hdcp=171 tee1=7008 tee2=6407 tee3=4706 tee4=NULL（湯の浦のtee4はNULL集計）

-- 4-3. HDCP の重複チェック（コース内で 1〜18 が1回ずつあるか）
SELECT gc.name,
       array_agg(ch.hdcp ORDER BY ch.hdcp) AS hdcp_sorted
  FROM public.golf_courses gc
  JOIN public.course_holes ch ON ch.course_id = gc.id
 WHERE gc.name IN (
   '鹿児島鹿屋カントリークラブ',
   '青島ゴルフ倶楽部',
   '湯の浦カントリー倶楽部'
 )
 GROUP BY gc.name
 ORDER BY gc.name;
-- 期待: 全コース {1,2,3,...,18}

-- 4-4. 各コースのホール詳細（目視確認用）
